public class LotteryDto {
	
	
	private String choice;
	private int buyed_money;
	private int result_money;
	
	private String result;
	
	public String getChoice() {
		return choice;
	}
	public void setChoice(String choice) {
		this.choice = choice;
	}
	public int getBuyed_money() {
		return buyed_money;
	}
	public void setBuyed_money(int buyed_money) {
		this.buyed_money = buyed_money;
	}
	public int getResult_money() {
		return result_money;
	}
	public void setResult_money(int result_money) {
		this.result_money = result_money;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	
	
}
